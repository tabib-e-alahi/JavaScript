var price  = 50

//Addition------------------------
price += 20 // shorthand for price = price + 20
console.log("After adding 20: ", price);

//Subtraction---------------------
price -= 30 // shorthand for price = price - 30
console.log("After subtracting 30: ", price);

//Multiplying---------------------
price *= 20 // shorthand for price = price * 20
console.log("After multiplying by 20: ", price);


//Division---------------------
price /= 5 // shorthand for price = price / 20
console.log("After dividing by 5: ", price);

// Modulus---------------------
var rem = price % 13 
console.log("Remainder: ", rem);

//Exponentiation------------

console.log(4**2)

